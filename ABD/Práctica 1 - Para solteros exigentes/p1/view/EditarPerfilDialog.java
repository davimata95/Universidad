	public void setDistancia(int distancia) {
		this.distancia = distancia;
		if (this.distancia != -1) {
			labelDistancia.setText("Distancia: " + distancia + "m");
		} else {
			labelDistancia.setText("Distancia: no expecificada");
		}
	}

