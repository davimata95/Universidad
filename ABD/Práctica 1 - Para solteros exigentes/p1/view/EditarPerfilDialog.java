    public EditarPerfilDialog(EditarPerfilController controller, Usuario usuario, boolean editable, double latO, double lonO) {
		this(null, true);
		this.controller = controller;
		perfilPanel1.setController(this.controller);
	
		this.usuario = usuario;
		perfilPanel1.rellenarVentana(usuario, editable);
		setEditable(editable);
		if (!editable) {
			setLatitudOrigen(latO);
			setLongitudOrigen(lonO);
			controller.determinarDistancia(this);
		}
	}

